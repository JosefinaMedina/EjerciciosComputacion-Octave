#Escriba un programa que determine si un dado
#entero (leído por consola) es primo
a=input("Ingrese numero a verificar: ")
m= 2

while m <= sqrt(a);
b=mod(a , m );  
  If b ~= 0
    disp("Es un numero primo")
  Elseif b=0
    print("El numero no es primo")
  End
  
   m=m+1;
end
printf("El numero es primo")

